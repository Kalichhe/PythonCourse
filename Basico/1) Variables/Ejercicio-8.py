
# Calculo del peso molecular

peso1 = float(input("Ingresa el peso 1: "))
atomo1 = float(input("Ingresa el atomo 1: "))
peso2 = float(input("Ingresa el peso 2: "))
atomo2 = float(input("Ingresa el atomo 2: "))

pesoMolecular = (peso1 * atomo1) + (peso2 * atomo2)

print("El peso molecular es: ", pesoMolecular)